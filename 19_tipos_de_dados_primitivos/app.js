/* string */
const name_0 = 'Leonardo';
const name_1 = "Leonardo";
const name_2 = `Leonardo`;
console.log(typeof name_0, name_0);
console.log(typeof name_1, name_1);
console.log(typeof name_2, name_2);

/* number */
const number_0 = 10;
const number_1 = 10.52;
console.log(typeof number_0, number_0);
console.log(typeof number_1, number_1);

/* undefined */
let name_student;
console.log(typeof name_student, name_student);

/* null */
const lastname_student = null;
console.log(typeof lastname_student, lastname_student);

/* boolean */
const approved_student = true;
const failed_student = false;
console.log(typeof approved_student, approved_student);
console.log(typeof failed_student, failed_student);
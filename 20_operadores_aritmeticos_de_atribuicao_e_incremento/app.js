/*
    arithmetic operators
        +   addition / concatenation
        -   subtraction
        *   multiplication
        /   division
        **  potentiation
        %   modulo

        order of precedence
            ()      parentheses
            **      exponentiation
            * / %   multiplication, division and modulo
            + -     addition and subtraction

    increment / decrement operators
        ++  increment operator
        --  decrement operator

    assignment operators
        +=
        -=
        *=
        /=
        %=
        **=
*/

